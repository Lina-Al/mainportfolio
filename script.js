  modToggle.className = "toggle";
  modToggle.textContent = mod.module;
  const subjContainer = document.createElement("div");
  subjContainer.className = "subject-container hidden";

  modToggle.onclick = () => {
    subjContainer.classList.toggle("hidden");
  };

  mod.subjects.forEach(subj => {
    const subjDiv = document.createElement("div");

    const subjTitle = document.createElement("div");
    subjTitle.className = "toggle";
    subjTitle.textContent = subj.name;
    const taskContainer = document.createElement("div");
    taskContainer.className = "task-container hidden";

    subjTitle.onclick = () => {
      taskContainer.classList.toggle("hidden");
    };

 subj.tasks.forEach(task => {
  const taskDiv = document.createElement("div");
  taskDiv.className = "task";

  // Определяем, что использовать — link или pdf
  const href = task.link && task.link !== "#" ? task.link : task.pdf;

  taskDiv.innerHTML = `
    <a href="${href}" target="_blank"><strong>${task.title}</strong></a>
  `;

  taskContainer.appendChild(taskDiv);
});

    subjDiv.appendChild(subjTitle);
    subjDiv.appendChild(taskContainer);
    subjContainer.appendChild(subjDiv);
  });

  modDiv.appendChild(modToggle);
  modDiv.appendChild(subjContainer);
  container.appendChild(modDiv);
